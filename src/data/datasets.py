class NeuralFieldDataset(Dataset):
    """
    Base Dataset for Physics-Informed Neural Field.
    Loads (N, 9) tensors generated by FeatureTensorizer.
    
    Tensor Channels:
    [0:3] x_rel, y_rel, z_abs
    [3:6] w, h, d
    [6]   semantic_type (1.0=Wire, 0.5=Pin)
    [7]   logic_flag (1.0=Target, 0.0=Aggressor)
    [8]   epsilon
    """
    def __init__(self, db_dir, dataframe, pad_size=None):
        self.db_dir = Path(db_dir)
        self.meta_df = dataframe.reset_index(drop=True)
        self.pad_size = pad_size if pad_size is not None else PAD_SIZE

    def __len__(self):
        return len(self.meta_df)

    def load_tensor(self, idx):
        row = self.meta_df.iloc[idx]
        filename = row['sample_filename']
        
        # [FIX] Strictly use design_name subfolder for loading pkl.gz as instructed by user
        if 'design_name' in row and pd.notna(row['design_name']):
            design_name = row['design_name']
        else:
            design_name = filename.split('__')[0]
            
        pkl_path = self.db_dir / design_name / filename
        
        if not pkl_path.exists():
            logger.error(f"File not found: {pkl_path}")
            return None
        
        try:
            with gzip.open(pkl_path, 'rb') as f:
                data = pickle.load(f)
            
            # 1. Load Tensor
            if isinstance(data, dict):
                cuboids = data.get('cuboids') # Expected (N, 9)
            else:
                logger.warning(f"Unknown data format in {filename}")
                return None
                
            if cuboids is None or len(cuboids) == 0:
                logger.warning(f"Empty cuboids in {filename}")
                return None
                
            # 2. Convert to Torch
            tensor = torch.from_numpy(cuboids).float()
            
            # 3. Padding / Truncation
            # Attention 연산을 위해 고정 길이로 맞춤 + Mask 생성
            names = np.array(data.get('cuboid_net_names', ["UNKNOWN"] * len(cuboids)))
            abs_geo = data.get('abs_geometries', np.zeros((len(cuboids), 6), dtype=np.float32))
            
            origin = data.get('origin', [0.0, 0.0, 0.0])
            W_X, W_Y = cfg.WINDOW_SIZE[0], cfg.WINDOW_SIZE[1]
            O_X, O_Y = cfg.TILING_OVERLAP, cfg.TILING_OVERLAP
            CORE_HW_X, CORE_HW_Y = (W_X - O_X) / 2.0, (W_Y - O_Y) / 2.0
            
            # [O(1) Vectorization] 수천 번의 파이썬 Loop를 C++ Tensor 연산 1번으로 압축
            w_h_d = tensor[:, 3:6]
            w, h, d = w_h_d[:, 0], w_h_d[:, 1], w_h_d[:, 2]
            lx = torch.tensor(abs_geo[:, 0] - origin[0], dtype=torch.float32)
            ly = torch.tensor(abs_geo[:, 1] - origin[1], dtype=torch.float32)
            
            l_max = torch.max(w_h_d, dim=1)[0]
            c_len = torch.zeros_like(l_max)
            
            mask_x = (w >= h) & (w >= d)
            mask_y = (h > w) & (h >= d)
            mask_z = ~(mask_x | mask_y) # Via
            
            # X-axis
            c_len[mask_x] = torch.clamp(
                torch.clamp(lx[mask_x] + w[mask_x]/2, max=CORE_HW_X) - torch.clamp(lx[mask_x] - w[mask_x]/2, min=-CORE_HW_X), min=0.0)
            # Y-axis
            c_len[mask_y] = torch.clamp(
                torch.clamp(ly[mask_y] + h[mask_y]/2, max=CORE_HW_Y) - torch.clamp(ly[mask_y] - h[mask_y]/2, min=-CORE_HW_Y), min=0.0)
            # Via
            via_mask = (torch.abs(lx) <= CORE_HW_X) & (torch.abs(ly) <= CORE_HW_Y)
            valid_via = mask_z & via_mask
            c_len[valid_via] = d[valid_via]
            
            safe_l_max = torch.clamp(l_max, min=1e-6)
            core_ratios = torch.where(l_max > 0, c_len / safe_l_max, torch.zeros_like(l_max)).numpy()
            target_net = self.meta_df.iloc[idx]['net_name']
            names_arr = np.array(names)
            tgt_mask = (names_arr == target_net)
            
            tgt_tensor, tgt_names = tensor[tgt_mask], names_arr[tgt_mask]
            tgt_abs_geo, tgt_core_ratios = abs_geo[tgt_mask], core_ratios[tgt_mask]
            
            # aggr_mask = ~tgt_mask
            # safe_pad = 512 # 최적의 VRAM과 Compile 속도를 위한 고정 사이즈
            # unique_aggrs = np.unique(names_arr[~tgt_mask])
            # aggr_tensors, aggr_names, aggr_abs_geos, aggr_core_ratios = [], [], [], []
            # for aggr in unique_aggrs[:64]: # VRAM 보호를 위해 최대 64개 넷으로 제한
            #     if aggr in ['PAD', 'UNKNOWN']: continue
            #     a_mask = (names_arr == aggr)
            #     a_cubs = tensor[a_mask]
                
            #     # 1. 넷의 전체 면적(Area) 계산하여 Feature에 주입
            #     areas = a_cubs[:, 3] * a_cubs[:, 4] * 2 + a_cubs[:, 4] * a_cubs[:, 5] * 2 + a_cubs[:, 5] * a_cubs[:, 3] * 2
            #     total_area_raw = areas.sum().item()
            #     total_area_log = np.log1p(total_area_raw)
                
            #     # 2. 골격 좌표 (Center 1개 + BBox 양극단 2개) = 총 3개 텐서로 압축
            #     mins = a_cubs[:, :3] - a_cubs[:, 3:6] / 2.0
            #     maxs = a_cubs[:, :3] + a_cubs[:, 3:6] / 2.0
            #     macro_min, macro_max = torch.min(mins, dim=0)[0], torch.max(maxs, dim=0)[0]
            #     macro_center = (macro_min + macro_max) / 2.0
                
            #     base_feats = a_cubs[:, 6:].mean(dim=0)
            #     base_feats[0] = total_area_log # 첫 번째 Feature 채널을 Total Area로 덮어쓰기
                
            #     pt_center = torch.cat([macro_center, (macro_max - macro_min), base_feats])
            #     pt_min = torch.cat([macro_min, torch.zeros(3), base_feats])
            #     pt_max = torch.cat([macro_max, torch.zeros(3), base_feats])
                
            #     aggr_tensors.extend([pt_center, pt_min, pt_max])
            #     aggr_names.extend([aggr] * 3)
                
            #     # Abs Geo & Core Ratio (대표값 복사)
            #     m_abs_center = np.mean(abs_geo[a_mask], axis=0)
            #     aggr_abs_geos.extend([m_abs_center] * 3)
            #     aggr_core_ratios.extend([np.max(core_ratios[a_mask])] * 3)
                
            # if len(aggr_tensors) > 0:
            #     aggr_tensor = torch.stack(aggr_tensors)
            #     tensor = torch.cat([tgt_tensor, aggr_tensor], dim=0)
            #     names = np.concatenate([tgt_names, aggr_names])
            #     abs_geo = np.concatenate([tgt_abs_geo, aggr_abs_geos])
            #     core_ratios = np.concatenate([tgt_core_ratios, aggr_core_ratios])
            # else:
            #     tensor, names, abs_geo, core_ratios = tgt_tensor, tgt_names, tgt_abs_geo, tgt_core_ratios
            aggr_mask = ~tgt_mask
            aggr_tensor, aggr_names = tensor[aggr_mask], names_arr[aggr_mask]
            aggr_abs_geo, aggr_core_ratios = abs_geo[aggr_mask], core_ratios[aggr_mask]
            
            # 1. Target Center
            target_center = tgt_tensor[:, :3].mean(dim=0) if len(tgt_tensor) > 0 else torch.zeros(3)
            
            # 2. Distance from Target to Aggressors (XY plane)
            dist_to_tgt = torch.norm(aggr_tensor[:, :2] - target_center[:2], dim=-1)
            
            # 3. Core Aggressors (< 2.0um): 정밀 커플링 계산을 위해 원형 100% 보존
            is_core = dist_to_tgt <= 2.0
            core_tensors, core_names = aggr_tensor[is_core], aggr_names[is_core.numpy()]
            core_abs_geo, core_core_ratios = aggr_abs_geo[is_core.numpy()], aggr_core_ratios[is_core.numpy()]
            
            # 4. Context Aggressors (> 2.0um): 메모리 사수 및 차폐막 보존을 위한 3D Grid Voxelization
            is_ctx = ~is_core
            ctx_tensors = aggr_tensor[is_ctx]
            
            ctx_res_tensors, ctx_res_names, ctx_res_abs_geo, ctx_res_core_ratios = [], [], [], []
            ctx_res_merged_names = []   # list[list[str]]: original net names merged into each voxel cell
            if len(ctx_tensors) > 0:
                ctx_names = aggr_names[is_ctx.numpy()]
                ctx_abs_geo = aggr_abs_geo[is_ctx.numpy()]
                ctx_core_ratios = aggr_core_ratios[is_ctx.numpy()]
                
                # Z축은 0.1um로 촘촘하게(층별 유전율 보존), XY는 1.0um로 넓게 해싱
                grid_hash = torch.round(ctx_tensors[:, :3] / torch.tensor([1.0, 1.0, 0.1]))
                unique_grids, inverse_indices = torch.unique(grid_hash, dim=0, return_inverse=True)
                
                for i in range(len(unique_grids)):
                    g_mask = (inverse_indices == i)
                    group = ctx_tensors[g_mask]
                    
                    merged_center = group[:, :3].mean(dim=0)
                    total_vol = (group[:, 3] * group[:, 4] * group[:, 5]).sum()
                    avg_z = group[:, 5].mean()
                    equiv_xy = torch.sqrt(total_vol / (avg_z + 1e-6))
                    
                    new_cuboid = group[0].clone()
                    new_cuboid[:3] = merged_center
                    new_cuboid[3:5] = equiv_xy
                    new_cuboid[5] = avg_z
                    new_cuboid[7] = 0.0 # Force Aggressor
                    # Z축이 동일하므로 group[0]의 Epsilon, Semantic Type 등은 안전하게 보존됨
                    
                    ctx_res_tensors.append(new_cuboid)
                    ctx_res_names.append('Voxel_Aggressor')
                    ctx_res_merged_names.append(list(ctx_names[g_mask.numpy()]))
                    ctx_res_abs_geo.append(np.mean(ctx_abs_geo[g_mask.numpy()], axis=0))
                    ctx_res_core_ratios.append(np.max(ctx_core_ratios[g_mask.numpy()]))
                    
            if len(ctx_res_tensors) > 0:
                ctx_tensor = torch.stack(ctx_res_tensors)
                tensor = torch.cat([tgt_tensor, core_tensors, ctx_tensor], dim=0)
                names = np.concatenate([tgt_names, core_names, ctx_res_names])
                abs_geo = np.concatenate([tgt_abs_geo, core_abs_geo, ctx_res_abs_geo])
                core_ratios = np.concatenate([tgt_core_ratios, core_core_ratios, ctx_res_core_ratios])
            else:
                tensor = torch.cat([tgt_tensor, core_tensors], dim=0)
                names = np.concatenate([tgt_names, core_names])
                abs_geo = np.concatenate([tgt_abs_geo, core_abs_geo])
                core_ratios = np.concatenate([tgt_core_ratios, core_core_ratios])
            N, C = tensor.shape[0], tensor.shape[1]
            # Use self.pad_size (= cfg.NF_PAD_TO_CUBOIDS = 1024) instead of the
            # previously hardcoded 512, which was silently discarding aggressor
            # context for large-net tiles and causing coupling under-prediction.
            safe_pad = self.pad_size
            if N > safe_pad:
                tensor, names, abs_geo = tensor[:safe_pad], names[:safe_pad], abs_geo[:safe_pad]
                core_ratios = core_ratios[:safe_pad]
                mask = torch.zeros(safe_pad, dtype=torch.bool)
            else:
                pad_len = safe_pad - N
                padding = torch.zeros((pad_len, C), dtype=torch.float32)
                tensor = torch.cat([tensor, padding], dim=0)
                names = np.concatenate([names, ["PAD"] * pad_len])
                abs_geo = np.concatenate([abs_geo, np.zeros((pad_len, 6), dtype=np.float32)])
                core_ratios = np.concatenate([core_ratios, np.zeros(pad_len, dtype=np.float32)])
                mask = torch.ones(safe_pad, dtype=torch.bool)
                mask[:N] = False

            meta_dict = {
                'cuboid_net_names': names,
                'abs_geometries': abs_geo,
                'core_ratios': core_ratios,
                'target_net_name': data.get('net_name', 'UNKNOWN'),
                'top_ports': data.get('top_ports', []),
                # 'global_segments': data.get('global_segments',[]),
                'origin': data.get('origin', np.zeros(3, dtype=np.float32)),
                'voxel_ctx_merged_names': ctx_res_merged_names,
            }
            if 'frw_ratio_matrix' in data:
                frw_mat = torch.tensor(data['frw_ratio_matrix'], dtype=torch.float32)
                if N < safe_pad:
                    # NxN 행렬을 safe_pad x safe_pad 로 우측/하단 패딩
                    pad_tuple = (0, safe_pad - N, 0, safe_pad - N)
                    frw_mat = torch.nn.functional.pad(frw_mat, pad_tuple, "constant", 0.0)
                meta_dict['frw_ratio_matrix'] = frw_mat
            return tensor, mask, meta_dict
            
        except Exception as e:
            logger.error(f"Failed to load {filename}: {e}")
            return None

class NeuralFieldSSLDataset(NeuralFieldDataset):
    """
    Dataset for Pre-training (No Labels)
    Returns: (cuboids_tensor, mask)
    """
    def __getitem__(self, idx):
        row = self.meta_df.iloc[idx]
        filename = row['sample_filename']
        design_name = row.get('design_name', filename.split('__')[0])
        pkl_path = self.db_dir / design_name / filename
        
        if not pkl_path.exists(): 
            logger.error(f"File not found: {pkl_path}")
            return None

        try:
            with gzip.open(pkl_path, 'rb') as f:
                data = pickle.load(f)
            cuboids = data.get('cuboids')
            if cuboids is None or len(cuboids) == 0: return None
            
            # SSL은 1024개면 충분함 (메모리 할당 및 복사 시간 1/4로 단축)
            pad_size = 256
            N, C = cuboids.shape
            
            if N > pad_size:
                tensor = torch.from_numpy(cuboids[:pad_size]).float()
                mask = torch.zeros(pad_size, dtype=torch.bool)
            else:
                tensor = torch.from_numpy(cuboids).float()
                padding = torch.zeros((pad_size - N, C), dtype=torch.float32)
                tensor = torch.cat([tensor, padding], dim=0)
                mask = torch.ones(pad_size, dtype=torch.bool)
                mask[:N] = False
                
            # SSL은 core_ratios, meta_dict 등이 전혀 필요 없으므로 빈 딕셔너리 반환!
            return tensor, mask, {}
        except Exception as e:
            return None

class NeuralFieldFinetuneDataset(NeuralFieldDataset):
    def __init__(self, data_dir, dataframe, pad_size=None):
        meta_df = dataframe.dropna(subset=['capacitance']).reset_index(drop=True)
        safe_pad_size = pad_size if pad_size is not None else 2048
        super().__init__(data_dir, meta_df, safe_pad_size)

    """Dataset for Fine-tuning (With Target, Ground, and Coupling Cap Labels)"""
    def __getitem__(self, idx):
        result = self.load_tensor(idx)
        if result is None: return None

        tensor, mask, meta_dict = result
        row = self.meta_df.iloc[idx]
        # CSV에서 로드될 경우 문자열로 변환된 딕셔너리를 복구하는 방어 로직
        cpl_caps = row.get('coupled_caps', {})
        if isinstance(cpl_caps, str):
            cpl_caps = ast.literal_eval(cpl_caps)
        cpl_caps = dict(cpl_caps)  # mutable copy for injection below

        # Inject summed coupling to voxelized context aggressors as 'Voxel_Aggressor'.
        # Without this, Voxel_Aggressor nodes have no matching label in robust_collate
        # (aggr_name_to_idx lookup returns -1) and receive zero gradient.
        voxel_merged = meta_dict.get('voxel_ctx_merged_names', [])
        if voxel_merged:
            all_ctx_names = [n for cell in voxel_merged for n in cell]
            voxel_cpl_sum = sum(
                cpl_caps.get(n.replace('\\', '').lstrip('\\'), 0.0)
                for n in all_ctx_names
            )
            if voxel_cpl_sum > 1e-6:
                cpl_caps['Voxel_Aggressor'] = voxel_cpl_sum

        labels_dict = {
            'total_cap': float(row.get('capacitance', 0.0)),
            'ground_cap': float(row.get('ground_cap', 0.0)),
            'coupled_caps': cpl_caps
        }
        return tensor, mask, labels_dict, meta_dict

def robust_collate(batch):
    """
    DataLoader용 Collate Function.
    None 데이터(로딩 실패)를 필터링하고, Dictionary들을 배치 단위(List)로 묶어줍니다.
    """
    batch =[b for b in batch if b is not None]
    if len(batch) == 0:
        return None
        
    elem = batch[0]
    
    # 1. SSL Dataset (tensor, mask, meta_dict)
    if len(elem) == 3:
        tensors = torch.stack([b[0] for b in batch])
        masks = torch.stack([b[1] for b in batch])
        
        meta_keys = batch[0][2].keys()
        meta_dict = {k: [b[2][k] for b in batch] for k in meta_keys}
        
        # Convert absolute geometries to tensor
        # meta_dict['abs_geometries'] = torch.tensor(np.array(meta_dict['abs_geometries'])).float()
        if 'abs_geometries' in meta_dict:
            meta_dict['abs_geometries'] = torch.tensor(np.array(meta_dict['abs_geometries'])).float()
        return tensors, masks, meta_dict
        
    # 2. Finetune Dataset (tensor, mask, labels_dict, meta_dict)
    elif len(elem) == 4:
        B = len(batch)
        tensors = torch.stack([b[0] for b in batch])
        masks = torch.stack([b[1] for b in batch])
        CURRENT_PAD = tensors.shape[1]
        A_tgt = torch.zeros(B, CURRENT_PAD, dtype=torch.float32)
        # Y_gnd = torch.zeros(B, dtype=torch.float32)
        # Y_total = torch.zeros(B, dtype=torch.float32)
        batch_core_ratios = torch.zeros(B, CURRENT_PAD, dtype=torch.float32)

        # Accumulate aggressor importance across batch to select top-N aggressors.
        # Dense designs (e.g., ldpc_decoder) can have 10K+ aggressors per net;
        # without a cap, A_aggr shape (B, 10K, 1024) × E edges → OOM.
        MAX_AGGR_BUDGET = getattr(cfg, 'MAX_AGGR_BUDGET', 256)
        aggr_importance: dict = {}
        for b in range(B):
            for k, v in batch[b][2].get('coupled_caps', {}).items():
                k = k.replace('\\', '')
                aggr_importance[k] = aggr_importance.get(k, 0.0) + float(v)
        if len(aggr_importance) > MAX_AGGR_BUDGET:
            batch_aggr_list = sorted(aggr_importance, key=lambda x: -aggr_importance[x])[:MAX_AGGR_BUDGET]
        else:
            batch_aggr_list = list(aggr_importance.keys())
        aggr_name_to_idx = {name: idx for idx, name in enumerate(batch_aggr_list)}

        MAX_AGGR = max(len(batch_aggr_list), 1)
        A_aggr = torch.zeros(B, MAX_AGGR, CURRENT_PAD, dtype=torch.float32)
        # Y_cpl = torch.zeros(B, MAX_AGGR, dtype=torch.float32)
        # valid_aggr_mask = torch.zeros(B, MAX_AGGR, dtype=torch.bool)

        net_name_to_id = {}
        batch_net_ids = torch.zeros(B, dtype=torch.long)
        current_net_id = 0
        for b in range(B):
            t_net = batch[b][3]['target_net_name'].replace('\\', '')
            if t_net not in net_name_to_id:
                net_name_to_id[t_net] = current_net_id
                current_net_id += 1
            batch_net_ids[b] = net_name_to_id[t_net]

        num_nets = current_net_id
        
        # 여기서 생성!
        Y_total_unique = torch.zeros(num_nets, dtype=torch.float32)
        Y_gnd_unique = torch.zeros(num_nets, dtype=torch.float32)
        Y_cpl_unique = torch.zeros(num_nets, MAX_AGGR, dtype=torch.float32)
        valid_aggr_mask_unique = torch.zeros(num_nets, MAX_AGGR, dtype=torch.bool)
        
        seen_nets = set()

        for b in range(B):
            lbl = batch[b][2]
            meta = batch[b][3]
            t_net = meta['target_net_name']
            # names = meta['cuboid_net_names']
            t_net = t_net.replace('\\', '')
            names = [n.replace('\\', '') for n in meta['cuboid_net_names']]
            core_ratio_tensor = torch.tensor(meta['core_ratios'], dtype=torch.float32)
            batch_core_ratios[b] = core_ratio_tensor
            net_id = net_name_to_id[t_net]
            if t_net not in seen_nets:
                seen_nets.add(t_net)
                Y_total_unique[net_id] = lbl['total_cap']
                Y_gnd_unique[net_id] = lbl['ground_cap']
                
                for aggr_name, cpl_val in lbl['coupled_caps'].items():
                    aggr_name = aggr_name.replace('\\', '')
                    if cpl_val < 1e-4: continue 
                    
                    k = aggr_name_to_idx.get(aggr_name, -1)
                    if 0 <= k < MAX_AGGR: 
                        Y_cpl_unique[net_id, k] = cpl_val
                        valid_aggr_mask_unique[net_id, k] = True

            # Target Vector 구축
            for i, name in enumerate(names):
                if name == t_net: A_tgt[b, i] = 1.0
                if name in aggr_name_to_idx:
                    k = aggr_name_to_idx[name]
                    A_aggr[b, k, i] = 1.0
        labels_dict = {
            'A_tgt': A_tgt, 'A_aggr': A_aggr, 
            'Y_cpl': Y_cpl_unique,          # 이름 변경 방지차 키는 그대로 두되 값 교체
            'Y_gnd': Y_gnd_unique, 
            'Y_total': Y_total_unique, 
            'valid_aggr_mask': valid_aggr_mask_unique,
            'core_ratios': batch_core_ratios,
            'batch_net_ids': batch_net_ids,
            'num_unique_nets': num_nets,
            'batch_aggr_list': batch_aggr_list,
        }
        
        meta_keys = batch[0][3].keys()
        meta_dict = {k: [b[3][k] for b in batch] for k in meta_keys}
        meta_dict['abs_geometries'] = torch.tensor(np.array(meta_dict['abs_geometries'])).float()
        if 'frw_ratio_matrix' in meta_keys and batch[0][3]['frw_ratio_matrix'] is not None:
            meta_dict['frw_ratio_matrix'] = torch.stack([b[3]['frw_ratio_matrix'] for b in batch])
        
        return tensors, masks, labels_dict, meta_dict


class NeuralFieldEvalDataset(NeuralFieldDataset):
    """
    Dataset for Inference/Evaluation.
    라벨(Label)은 요구하지 않지만, 풀칩 조립(Assembly)을 위한 core_ratios 등 meta_dict를 온전히 반환합니다.
    """
    def __getitem__(self, idx):
        result = self.load_tensor(idx)
        if result is None: return None
        tensor, mask, meta_dict = result
        return tensor, mask, meta_dict
