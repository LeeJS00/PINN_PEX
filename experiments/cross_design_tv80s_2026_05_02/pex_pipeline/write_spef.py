"""Lumped per-net SPEF writer (IEEE 1481-1999 compatible subset).

Format:
  Header (units, vendor, divider, etc.)
  For each net:
    *D_NET <name> <total_cap>
    *CONN
    *P <name>:1 O *C <x> <y>
    *CAP
    1 <name>:1 <c_gnd>
    2 <name>:1 <agg1>:1 <c_pair_1>
    ...
    *RES
    1 <name>:1 <name>:2 <r_total>
    *END

Compatible with src/evaluation/compare_spef.py (which sums *CAP lines per net).

Use:
    from pex_pipeline.write_spef import LumpedSPEFWriter
    w = LumpedSPEFWriter(design_name='tv80s', vendor='PINNPEX', program='PINNPEX-EDA')
    w.write(out_path, nets=net_list)
where net_list is a list of dicts with keys:
    name, total_cap, c_gnd, pairs (list of (agg_name, c_pair)), total_r, port_xy
"""
from __future__ import annotations

import datetime
from pathlib import Path
from typing import Dict, List, Tuple

import numpy as np


class LumpedSPEFWriter:
    HEADER_TPL = '''*SPEF "IEEE 1481-1999"
*DESIGN "{design}"
*DATE "{date}"
*VENDOR "{vendor}"
*PROGRAM "{program}"
*VERSION "{version}"
*DESIGN_FLOW "PIN_CAP NONE" "NAME_SCOPE LOCAL"
*DIVIDER /
*DELIMITER :
*BUS_DELIMITER []
*T_UNIT 1.0 NS
*C_UNIT 1.0 FF
*R_UNIT 1.0 OHM
*L_UNIT 1.0 HENRY

// Generated by PINNPEX (lumped SPEF mode)
'''

    def __init__(self, design_name: str = "design",
                 vendor: str = "PINNPEX",
                 program: str = "PINNPEX-EDA",
                 version: str = "v1.0"):
        self.design_name = design_name
        self.vendor = vendor
        self.program = program
        self.version = version

    def _format_header(self) -> str:
        return self.HEADER_TPL.format(
            design=self.design_name,
            date=datetime.datetime.now().strftime("%a %b %d %H:%M:%S %Y"),
            vendor=self.vendor,
            program=self.program,
            version=self.version,
        )

    def _format_net(self, net: dict) -> str:
        name = net["name"]
        total = float(net["total_cap"])
        c_gnd = max(0.0, float(net.get("c_gnd", 0.0)))
        pairs = net.get("pairs", [])
        total_r = float(net.get("total_r", 0.0))
        port_xy = net.get("port_xy", (0.0, 0.0))

        pair_lines = []
        cap_id = 1
        # Ground cap on port node
        pair_lines.append(f"{cap_id} {name}:1 {c_gnd:.6g}")
        cap_id += 1
        # Coupling caps
        for agg_name, c_pair in pairs:
            if c_pair <= 0:
                continue
            pair_lines.append(f"{cap_id} {name}:1 {agg_name}:1 {float(c_pair):.6g}")
            cap_id += 1

        cap_section = "\n".join(pair_lines)

        # Resistance: single lumped segment (port → sink); use 2-node pseudo-segment
        if total_r > 0:
            res_section = f"\n*RES\n1 {name}:1 {name}:2 {total_r:.6g}\n"
        else:
            res_section = ""

        return (
            f"\n*D_NET {name} {total:.6g}\n"
            f"*CONN\n"
            f"*P {name}:1 O *C {port_xy[0]:.4f} {port_xy[1]:.4f}\n"
            f"*N {name}:2 *C {port_xy[0]:.4f} {port_xy[1]:.4f}\n"
            f"*CAP\n{cap_section}\n"
            f"{res_section}"
            f"*END\n"
        )

    def write(self, out_path: Path | str, nets: List[dict]) -> None:
        out_path = Path(out_path)
        out_path.parent.mkdir(parents=True, exist_ok=True)
        with open(out_path, "w") as f:
            f.write(self._format_header())
            for net in nets:
                f.write(self._format_net(net))


def write_spef_from_records(out_path, design_name: str, records: list) -> None:
    """Convenience function. records: list of dicts with required keys."""
    w = LumpedSPEFWriter(design_name=design_name)
    w.write(out_path, records)


